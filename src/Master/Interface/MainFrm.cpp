void CMainFrame::OnSysCommand(UINT nID, LPARAM lParam)
{
	if(nID == SC_CLOSE)
	{
		if ( !((CInterfaceApp*)AfxGetApp())->GetActiveDoc()->DoOperatorAuthentication(0) )
			return;
	}

	CMDIFrameWnd::OnSysCommand(nID, lParam);
}
